					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">ETAPA 4</span>
						<h2>Maturação</h2>
						<p><span>Esse &ldquo;new make&rdquo; do destilado é então transferido para maturar em barris de carvalho.</span> A madeira do carvalho não é uniforme, portanto, o uísque de diferentes barris terá seu pico em diferentes momentos.</p>
