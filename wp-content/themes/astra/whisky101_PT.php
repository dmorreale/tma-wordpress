				<div class="stepTwoColumn step02 stepFlip">
					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">ETAPA 2</span>
						<h2>Fermentação</h2>
						<p><span>O mosto é resfriado e colocado em um tanque chamado washback, onde a levedura  é adicionada.</span> A levedura converte os açúcares em álcool e CO<sup>2</sup>. Nesse processo, é criada uma cerveja forte (9% ABV), denominada <em>wash</em>.</p>
					</div>
					<div class="stepRight dlanim percentFaderDefault">
						<img src="/wp-content/themes/astra/lite_academy/images/step02_graphic_v2.png" alt="">
