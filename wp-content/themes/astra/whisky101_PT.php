						<span class="steplabel">ETAPA 5</span>
						<h2>Homogeneização e<br>engarrafamento</h2>
						<p><span>Depois que o uísque atinge o pico da maturidade,</span> os barris são esvaziados e os uísques de barris de idade semelhante são misturados por vários meses antes do engarrafamento.</p>
