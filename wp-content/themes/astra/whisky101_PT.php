				<div class="stepTwoColumn step03">
					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">ETAPA 3</span>
						<h2>Destilação</h2>
						<p><span>O <em>wash</em> é destilado duas vezes em alambiques de cobre, para separar o álcool da água.</span> Apenas o álcool de melhor qualidade, denominado &ldquo;new make&rdquo;, é selecionado nesses alambiques.</p>
