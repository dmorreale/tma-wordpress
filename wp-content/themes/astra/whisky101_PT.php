					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">ETAPA 1</span>
						<h2>Infusão <br>e maceração</h2>
						<p><span>A cevada é repetidamente submergida em água,</span> e em seguida é colocada em um forno para secar. Os grãos de cevada são  triturados e covertidos em uma farinha grossa denominada <em>grist</em>. O <em>grist</em> é então misturado em água morna. O amido se decompõe em açúcares, que se dissolvem na água criando uma solução denominada mosto.</p>
					</div>
					<div class="stepRight dlanim percentFaderDefault">
						<img src="/wp-content/themes/astra/lite_academy/images/step01_graphic_v2.png" alt="">
