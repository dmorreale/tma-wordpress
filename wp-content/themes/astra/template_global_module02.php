							<span class="steplabel">Step 2</span>
							<h2>Cooperage<span>Making the Casks</span></h2>
							<p>The Macallan has long-standing partnerships with cooperages in Jerez, Spain. Wood is sprayed with water, heated and bent into cask shapes before being "toasted". This process prepares the cask to allow the whisky to extract flavours and colour from the wood during maturation.</p>
