								<p>The Macallan&rsquo;s oak casks are the single greatest contributor to the outstanding quality of The Macallan. Up to 80% of the aromas and flavours and 100% of the colour in The Macallan comes from these exceptional casks. After the oak is air dried for 2 years and the casks are handcrafted to our own specifications in Spain, the casks are seasoned with sherry wine for up to 18 months before they are shipped up to Scotland and filled with The Macallan new make spirit. The entire cask-making process, which takes around 5 years, is done under the watchful eyes of The Macallan Master of Wood.</p>
