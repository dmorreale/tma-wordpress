					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">1</span>
						<h2>Maltage et brassage</h2>
						<p><span>L’orge est maltée pour permettre à l’amidon qu’elle contient de devenir accessible. Le processus de maltage implique trempage, germination et séchage.</span> Les grains d’orge maltée sont broyés et moulus en une farine grossière appelée grist. Le grist est mélangé avec de l’eau chaude dans le mash tun. L’amidon est solubilisé et décomposé en sucres, créant une solution appelée moût.</p>
					</div>
					<div class="stepRight dlanim percentFaderDefault">
						<img src="/wp-content/themes/astra/lite_academy/images/step01_graphic_v2.png" alt="">
