					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">4</span>
						<h2>Vieillissement</h2>
						<p><span>Ce distillat – new make spirit – est ensuite transféré dans des fûts en chêne pour y être vieilli</span> Le bois de chêne n’est pas homogène, donc différents fûts donneront leur meilleur whisky à des moments différents.</p>
					</div>
