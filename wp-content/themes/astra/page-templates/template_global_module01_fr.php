				<div class="stepTwoColumn step02 stepFlip">
					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">2</span>
						<h2>Fermentation</h2>
						<p><span>Le wort est refroidi et placé dans un washback, dans lequel la levure est ajoutée.</span> Ainsi, les sucres sont transformés en alcool et en CO₂. On obtient alors une bière forte (environ 9 %vol) appelée wash.</p>
					</div>
					<div class="stepRight dlanim percentFaderDefault">
						<img src="/wp-content/themes/astra/lite_academy/images/step02_graphic_v2.png" alt="">
					</div>
				</div>
				<img class="stepImg stepImg03 dlanim percentFaderDefault" src="/wp-content/themes/astra/lite_academy/images/step03_french.svg" alt="">
				<img class="stepImg_m stepImg03_m dlanim percentFaderDefault" src="/wp-content/themes/astra/lite_academy/images/step03_french.svg" alt="">
				<div class="stepTwoColumn step03">
					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">3</span>
						<h2>Distillation</h2>
						<p><span>Le wash est (généralement) distillé deux fois dans des alambics en cuivre de type pot still pour séparer l’alcool de l’eau.</span> Seul le tout meilleur distillat issu de ces alambics, appelé new make spirit, est sélectionné.</p>
						<!--<a href="#" class="moreInfoBtn">More Info</a>-->
					</div>
					<div class="stepRight dlanim percentFaderDefault">
