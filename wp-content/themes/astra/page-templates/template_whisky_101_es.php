				<img class="stepImg_m stepImg03_m dlanim percentFaderDefault" src="/wp-content/themes/astra/lite_academy/images/step03_m_spanish.svg" alt="">
				<div class="stepTwoColumn step03">
					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">3</span>
						<h2>Destilación</h2>
						<p>La colada se destila dos veces (usualmente) en alambiques de cobre para separar el alcohol del agua. Tan solo el espirituoso de mayor calidad, llamado “new make”, es seleccionado de estos alambiques. </p>
