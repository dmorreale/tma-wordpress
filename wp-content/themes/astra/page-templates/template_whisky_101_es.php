						<span class="steplabel">4</span>
						<h2>Maturación</h2>
						<p><span>Este espirituoso nuevo es transferido a barricas de roble para la maduración.</span> El roble no es uniforme por lo que el whisky en diferentes barricas alcanza su punto máximo en tiempos diferentes. </p>
					</div>
