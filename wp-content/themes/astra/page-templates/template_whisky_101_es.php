				<img class="stepImg_m stepImg02_m dlanim percentFaderDefault" src="/wp-content/themes/astra/lite_academy/images/step02_m_spanish.svg" alt="">
				<div class="stepTwoColumn step02 stepFlip">
					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">2</span>
						<h2>Fermentación</h2>
						<p><span>El mosto se enfría y se pone en un tanque llamado washback, al cual se añade levadura de destilación.</span> Esto convierte el azúcar en alcohol y  CO2. Se crea una cerveza fuerte (aprox. 9% ALC/VOL) llamada colada. </p>
					</div>
					<div class="stepRight dlanim percentFaderDefault">
						<img src="/wp-content/themes/astra/lite_academy/images/step02_graphic_v2.png" alt="">
					</div>
				</div>
