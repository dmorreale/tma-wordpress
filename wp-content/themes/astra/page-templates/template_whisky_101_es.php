					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">1</span>
						<h2>Malteado &amp; Macerado</h2>
						<p>La cebada se maltea para permitir que el almidón que contiene sea accesible. El proceso de malteado implica maceración, germinación y horneado. Los granos de cebada malteada se trituran y muelen hasta obtener una harina gruesa llamada molienda. La molienda se mezcla con agua caliente en el mash tun. El almidón se solubiliza y se descompone en azúcares, creando una solución llamada mosto.</p>
					</div>
					<div class="stepRight dlanim percentFaderDefault">
						<img src="/wp-content/themes/astra/lite_academy/images/step01_graphic_v2.png" alt="">
					</div>
