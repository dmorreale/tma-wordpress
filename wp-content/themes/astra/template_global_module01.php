					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">4</span>
						<h2>Maturation</h2>
						<p><span>This new make spirit is then transferred into oak casks for maturation.</span> Oak is not uniform so whisky in different casks will peak at different times.</p>
					</div>
					<div class="stepRight dlanim percentFaderDefault">
