				<div class="stepTwoColumn step03">
					<div class="stepLeft dlanim percentFaderDefault">
						<span class="steplabel">3</span>
						<h2>Distillation</h2>
						<p><span>The wash is distilled twice (usually) in copper pot stills to separate the alcohol from the water.</span> Only the finest quality spirit, called &ldquo;new make,&rdquo; is selected from these stills.</p>
						<!--<a href="#" class="moreInfoBtn">More Info</a>-->
					</div>
					<div class="stepRight dlanim percentFaderDefault">
