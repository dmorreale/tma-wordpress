						<span class="steplabel">1</span>
						<h2>Malting &amp;<br>Mashing</h2>
						<p><span>Barley is malted to enable the starch within it to become accessible. The malting process involves steeping, germination and kilning. </span>Malted barley grains are crushed and ground into a coarse flour called grist. The grist is mixed with hot water in the mash tun. Starch is solubilised and broken down into sugars, , creating a solution called wort.</p>
					</div>
					<div class="stepRight dlanim percentFaderDefault">
						<img src="/wp-content/themes/astra/lite_academy/images/step01_graphic_v2.png" alt="">
